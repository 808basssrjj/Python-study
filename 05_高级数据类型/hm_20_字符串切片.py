num_str = "0123456789"

# 截取从2-5的字符串
print(num_str[2:6])

# 截取从2-末尾的字符串
print(num_str[2:])

# 截取从开头-5的字符串
print(num_str[0:6])

# 截取完整的字符串
print(num_str[:])

# 从开始位置每隔一个字符截取
print(num_str[::2])

# 从1每隔一个字符截取
print(num_str[1::2])

# 截取从2-末尾-1
print(num_str[2:-1])

# 截取末尾两个
print(num_str[-2:])

# 字符串逆序
print(num_str[::-1])
